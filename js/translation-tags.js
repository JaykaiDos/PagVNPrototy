/**
 * Normaliza un string para búsqueda: trim + lowercase + colapso de espacios.
 *
 * @param {string} raw
 * @returns {string}
 */
function _normalizeKey(raw) {
  return String(raw).trim().toLowerCase().replace(/\s+/g, ' ');
}

