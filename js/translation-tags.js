/**
 * Traduce una lista de tags de VNDB al español.
 *
 * Estrategia de búsqueda (en orden de prioridad):
 *  1. Coincidencia exacta en TAGS_ES (respeta capitalización de VNDB).
 *  2. Coincidencia normalizada via _normalizedMap (case-insensitive).
 *  3. Fallback graceful: devuelve el original + lo registra en _missingTags.
 *
 * Garantías:
 *  - Filtra valores vacíos/nulos antes de procesar.
 *  - Elimina duplicados (sinónimos que colapsan a la misma traducción).
 *  - Nunca lanza excepción.
 *
 * @param {string[]} tags  Tags en inglés provenientes de la API de VNDB.
 * @returns {string[]}     Tags traducidos al español, sin duplicados.
 *
 * @example
 * translateTags(['Action', 'Fantasy', 'School Life']);
 * // → ['Acción', 'Fantasía', 'Vida Cotidiana']
 */
function translateTags(tags = []) {
  if (!Array.isArray(tags)) return [];

  const seen   = new Set();
  const result = [];

  tags.forEach((raw) => {
    const key = String(raw ?? '').trim();
    if (!key) return;

    // 1. Coincidencia exacta
    let translation = TAGS_ES[key];

    // 2. Coincidencia normalizada (case-insensitive)
    if (translation === undefined) {
      translation = _normalizedMap.get(_normalizeKey(key));
    }

    // 3. Fallback: conserva el original y lo acumula para descarga posterior
    if (translation === undefined) {
      if (!_missingTags.has(key)) {
        _missingTags.add(key);
        console.warn(`[Tags] Sin traducción: "${key}" — usa VNHub.downloadMissingTags() para exportar todos`);
      }
      translation = key;
    }

    // Deduplicar: evita mostrar dos veces el mismo texto traducido
    if (!seen.has(translation)) {
      seen.add(translation);
      result.push(translation);
    }
  });

  return result;
}

/**
 * Devuelve un array con todos los tags sin traducción encontrados
 * desde la última carga de página, ordenados alfabéticamente.
 *
 * @returns {string[]}
 */
function getMissingTags() {
  return [..._missingTags].sort((a, b) => a.localeCompare(b));
}

